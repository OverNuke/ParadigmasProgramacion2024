% Reglas
%Padre
es_padre_de(Padre,Hijo) :- 
progenitor(Padre,Hijo)
, hombre(Padre).
%Madre
es_madre_de(Madre,Hijo) :- 
progenitor(Madre,Hijo)
, mujer(Madre).
% Abuelo--------------------------------------------------------
es_abuelo_de(Abuelo,Nieto) :- 
progenitor(Abuelo,Padre)
, progenitor(Padre,Nieto)
, hombre(Abuelo).
% Abuela--------------------------------------------------------
es_abuela_de(Abuela,Nieto) :- 
progenitor(Abuela,Padre)
, progenitor(Padre,Nieto)
, mujer(Abuela).
% Hermano--------------------------------------------------------
es_hermano_de(Hermano01,Hermano02) :-
hombre(Hermano02)
, (es_padre_de(Padre, Hermano01),es_padre_de(Padre,Hermano02))
; (es_madre_de(Madre,Hermano01), es_madre_de(Madre, Hermano02))
, Hermano01\=Hermano02.
% Hermana--------------------------------------------------------
es_hermana_de(Hermano,Hermana) :- mujer(Hermana)
, (es_padre_de(Padre, Hermano),es_padre_de(Padre,Hermana))
; (es_madre_de(Madre,Hermano) ,es_madre_de(Madre,Hermana))
, Hermano\=Hermana.
% Nieto & Nieta--------------------------------------------------------
es_nieto_de(Nieto,Abuelo) :- es_abuelo_de(Abuelo, Nieto); es_abuela_de(Abuelo,Nieto).
% Pareja--------------------------------------------------------
es_pareja_de(X,Y) :-
pareja(X,Y); pareja(Y,X).
% Primo & Prima--------------------------------------------------------
es_primx_de(X, Primo) :-
(progenitor(Padre,X), progenitor(Tio,Primo); es_pareja_de(Primo, Pareja); es_pareja_de(X,Pareja02))
, (es_hermano_de(Padre,Tio); es_hermana_de(Padre,Tio)
; progenitor(Tio,Pareja); progenitor(Padre,Pareja02)).
% Sobrino--------------------------------------------------------
es_sobrino_de(Tio,Sobrino) :- 
(es_padre_de(Padre,Tio)
; es_padre_de(Padre,Pareja)
, es_pareja_de(Tio,Pareja)),
es_abuelo_de(Padre, Sobrino).

